/**
 * MagicalRow
 */
public class MagicalRow {

    public static void main(String[] args) {
        
        int[][] a={{1,2,3},{2,4,6},{3,5,6}};
        for(int i=0;i<3;i++){
            for(int j=0;j<3;j++){
                System.out.println(a[i][j]);
            }
            
        }
    }
}